// Display labels matching the editor canvas come from each node's services.json
// `title`. These overrides cover nodes whose first service title is unhelpful
// (multi-service nodes whose first variant isn't representative) or missing
// (no services.json). Everything else uses the service title verbatim.
const NODE_LABEL_OVERRIDES = {
	core: 'Core',
	index_search: 'Index Search',
	response: 'Response',
	webhook: 'Webhook',
	tool_mcp_client: 'MCP Client',
	llm_ibm_watson: 'IBM Watson',
};

const LABEL_ACRONYMS = { llm: 'LLM', ai: 'AI', api: 'API', db: 'DB', ocr: 'OCR', ner: 'NER', mcp: 'MCP', http: 'HTTP', tts: 'TTS', ibm: 'IBM', url: 'URL', id: 'ID' };

/** Last-resort label from a node directory name: `tool_http_request` -> `Tool HTTP Request`. */
function prettifyName(name) {
	return name
		.split('_')
		.map((w) => LABEL_ACRONYMS[w] || w.charAt(0).toUpperCase() + w.slice(1))
		.join(' ');
}

/** classType + display title + first-sentence description for a node, from its first services*.json (static regex, no JSON parse). */
async function readNodeMeta(nodeDir) {
	const svc = (await glob('services*.json', { cwd: nodeDir, nodir: true })).sort()[0];
	if (!svc) return { classType: '', title: '', description: '' };
	const text = await readFile(path.join(nodeDir, svc));
	const ctm = /"classType"\s*:\s*\[([^\]]*)\]/.exec(text);
	const classType = ctm ? (ctm[1].match(/"([^"]*)"/) || [])[1] || '' : '';
	const title = (/"title"\s*:\s*"([^"]*)"/.exec(text) || [])[1] || '';
	return { classType, title, description: extractDescription(text) };
}

/** Extract a first-sentence description from a raw services*.json text string. */
function extractDescription(text) {
	const m = /"description"\s*:\s*\[([^\]]*)\]/.exec(text);
	if (!m) return '';
	const parts = m[1].match(/"((?:[^"\\]|\\.)*)"/g) || [];
	const full = parts.map((s) => s.slice(1, -1)).join('').trim();
	const dot = full.indexOf('. ');
	return dot >= 0 ? full.slice(0, dot + 1) : full;
}

/**
 * Map of service slug -> first-sentence description for all services*.json in a node
 * directory. Used to resolve descriptions for variant sub-pages.
 *   services.chat.json  -> slug 'chat'
 *   services.agent.json -> slug 'agent'
 */
async function readServiceDescriptions(nodeDir) {
	const svcs = await glob('services*.json', { cwd: nodeDir, nodir: true });
	const map = new Map();
	for (const svc of svcs) {
		// Strip `.json` before the `services` prefix so the base manifest
		// (`services.json`) yields an empty slug and is skipped; stripping the
		// prefix first would leave `json`.
		const slug = svc.replace(/\.json$/, '').replace(/^services\.?/, '');
		if (!slug) continue;
		const desc = extractDescription(await readFile(path.join(nodeDir, svc)));
		if (desc) map.set(slug, desc);
	}
	return map;
}

/**
 * Find the best description for a variant from the parent node's service description map.
 * Match order: exact slug, then ends-with `_slug`, then variant starts-with slug. Within
 * each tier the longest matching slug wins, so overlapping slugs (e.g. `parse`/`parser`)
 * resolve deterministically regardless of Map insertion order.
 * @param {string} variant - variant name to resolve a description for.
 * @param {Map<string, string>} serviceDescriptions - slug -> description map.
 * @return {string} the matched description, or '' if none match.
 */
function variantDescription(variant, serviceDescriptions) {
	if (serviceDescriptions.has(variant)) return serviceDescriptions.get(variant);
	const longestMatch = (pred) => {
		let best = null;
		let bestLen = -1;
		for (const [slug, desc] of serviceDescriptions) {
			if (pred(slug) && slug.length > bestLen) {
				best = desc;
				bestLen = slug.length;
			}
		}
		return best;
	};
	return longestMatch((slug) => variant.endsWith('_' + slug)) ?? longestMatch((slug) => variant.startsWith(slug)) ?? '';
}

/** Canvas-style sidebar/page label for a node (override > service title > prettified name). */
function nodeLabel(name, serviceTitle) {
	return NODE_LABEL_OVERRIDES[name] || serviceTitle || prettifyName(name);
}

/** Double-quote a YAML scalar so titles with `:` `(` `/` stay safe. */
function yamlStr(v) {
	return `"${String(v).replace(/\\/g, '\\\\').replace(/"/g, '\\"')}"`;
}

/**
 * Stage a node's markdown for the docs site: guarantee a `slug` (keeps the flat
 * /nodes/<name> route under category nesting), set a `title` when the doc lacks
 * one, and drop the body's leading `# H1` so the page shows a single title (the
 * theme renders the title from front matter; a body H1 would duplicate it).
 * The generated "## Source" section is lifted into `source_url` front matter —
 * the site renders it as a "View source" breadcrumb action (DocBreadcrumbs
 * swizzle) instead of a content section. Everything else in README.md renders
 * verbatim, so the page, README.md, and the LLM .md surface carry the same content.
 */
function stageNodeMarkdown(content, { slug, title }) {
	let fmLines = [];
	let body = content;
	const fm = /^---\r?\n([\s\S]*?)\r?\n---\r?\n?/.exec(content);
	if (fm) {
		fmLines = fm[1].split(/\r?\n/);
		body = content.slice(fm[0].length);
	}
	// Drop a single leading top-level heading (`# Title`, not `## ...`).
	body = body.replace(/^(?:\s*\r?\n)*#(?!#)\s+[^\n]*\r?\n+/, '');
	// Lift the generated "View source" link line (optionally under a Source
	// heading, for older blocks) out of the body.
	let sourceUrl = null;
	body = body.replace(/^(?:##+\s+Source\r?\n+)?\[[^\]]*View source[^\]]*\]\((https?:[^)\s]+)\)\r?\n?/m, (_, url) => {
		sourceUrl = url;
		return '';
	});
	// Tidy: a node with no dependencies leaves an empty generated block behind —
	// drop the bare markers and any divider that introduced them.
	body = body.replace(/(?:^---\s*\r?\n+)?<!-- ROCKETRIDE:GENERATED:PARAMS START -->\s*(?:<!--(?:[^-]|-(?!->))*-->\s*)*<!-- ROCKETRIDE:GENERATED:PARAMS END -->\s*$/m, '');
	const has = (k) => fmLines.some((l) => new RegExp(`^${k}\\s*:`).test(l));
	const inject = [];
	if (!has('slug')) inject.push(`slug: ${yamlStr(slug)}`);
	if (title != null && !has('title')) inject.push(`title: ${yamlStr(title)}`);
	if (sourceUrl && !has('source_url')) inject.push(`source_url: ${yamlStr(sourceUrl)}`);
	const merged = [...inject, ...fmLines].filter((l) => l.trim() !== '');
	return `---\n${merged.join('\n')}\n---\n\n${body}`;
}

/**
 * Normalize a filesystem path to forward slashes for use in doc ids and routes.
 * @param {string} p - a path that may contain platform-specific separators.
 * @return {string} the path with `/` separators.
 */
function toPosix(p) {
	return p.split(path.sep).join('/');
}

/** Extract a `title:` from a leading YAML front-matter block, if present. */
function frontMatterTitle(content) {
	const m = /^---\r?\n([\s\S]*?)\r?\n---/.exec(content);
	if (!m) return null;
	const t = /(^|\n)title:\s*(.+?)\s*(\n|$)/.exec(m[1]);
	return t ? t[2].replace(/^['"]|['"]$/g, '') : null;
}

/** First markdown heading, as a title fallback. */
function headingTitle(content) {
	const m = /^#\s+(.+?)\s*$/m.exec(content);
	return m ? m[1] : null;
}

/**
 * Compute the Docusaurus doc id (== route minus leading slash) for a file at
 * `rel` under `mount`. `index` files collapse to their directory.
 */
function docIdFor(mount, rel) {
	let relNoExt = rel.replace(/\.(md|mdx)$/i, '');
	const base = path.posix.basename(relNoExt);
	if (base === 'index') {
		const dir = path.posix.dirname(relNoExt);
		relNoExt = dir === '.' ? '' : dir;
	}
	return relNoExt ? `${mount}/${relNoExt}` : mount;
}

/** Discover contributing packages from the registry's declared `docs` mounts. */
function discoverContributors() {
	const registry = require('../../../../scripts/lib/registry');
	const contributors = [];
	for (const name of registry.names()) {
		const mod = registry.get(name);
		if (!mod || !Array.isArray(mod.docs)) continue;
		// mod._path is <pkg>/scripts. pnpm may have registered the module via a
		// symlinked copy under node_modules, so resolve to the real location.
		let scriptsDir = mod._path;
		try {
			scriptsDir = require('fs').realpathSync(mod._path);
		} catch {
			/* keep mod._path */
		}
		const pkgRoot = path.dirname(scriptsDir);
		for (const entry of mod.docs) {
			if (!entry || !entry.mount) continue;
			const mount = String(entry.mount).replace(/^\/+|\/+$/g, '');
			if (!isValidMount(mount)) {
				throw new Error(`docs:gather: module "${name}" declares mount "${entry.mount}" which does not resolve to a spine slot.\n  Valid slots: ${mountSlots().join(', ')}`);
			}
			contributors.push({ module: name, mount, sourceDir: path.join(pkgRoot, entry.source || 'docs') });
		}
	}
	return contributors;
}

/** Remove generated artifacts from static/ (keep .gitkeep and img/). */
async function clearGeneratedStatic(staticDir) {
	if (!(await exists(staticDir))) return;
	for (const name of await readDir(staticDir)) {
		if (name === '.gitkeep' || name === 'img') continue;
		await rm(path.join(staticDir, name));
	}
}

/**
 * Stage one doc file into the assembled content tree and write its raw pre-MDX
 * sibling for the LLM surface.
 * @param {object} args
 * @param {string} args.srcAbs - absolute source path.
 * @param {string} args.destAbs - absolute destination path in the content tree.
 * @param {string} args.siblingAbs - absolute path for the raw `.md` sibling.
 * @param {string} args.content - raw file content (for the sibling).
 * @param {'copy'|'symlink'} args.mode - copy or symlink the source into place.
 * @return {Promise<void>}
 */
async function stageFile({ srcAbs, destAbs, siblingAbs, content, mode }) {
	await mkdir(path.dirname(destAbs));
	if (mode === 'symlink') {
		await rm(destAbs);
		try {
			await symlink(srcAbs, destAbs, 'file');
		} catch {
			await copyFile(srcAbs, destAbs); // Windows symlink may need privilege
		}
	} else {
		await copyFile(srcAbs, destAbs);
	}
	// Raw pre-MDX sibling for the LLM surface.
	await writeFileEnsure(siblingAbs, content);
}

/**
 * @param {object} args
 * @param {string} args.projectRoot
 * @param {string} args.contentStaticDir  shell-authored spine pages
 * @param {string} args.contentDir        assembled output Docusaurus reads
 * @param {string} args.staticDir         where raw .md siblings are written
 * @param {'copy'|'symlink'} [args.mode]
 * @param {object} [args.task]
 * @returns {Promise<Array>} manifest entries
 */
async function gather({ projectRoot, contentStaticDir, contentDir, staticDir, mode = 'copy', task }) {
	await rm(contentDir);
	await mkdir(contentDir);
	await clearGeneratedStatic(staticDir);

	const routes = new Map(); // docId -> source (collision guard)
	const manifest = [];

	function claim(docId, source) {
		if (routes.has(docId)) {
			throw new Error(`docs:gather: route collision at "/${docId}"\n  between ${routes.get(docId)}\n  and     ${source}`);
		}
		routes.set(docId, source);
	}

	// 1. Shell-authored static pages (copied first; they own the spine).
	if (await exists(contentStaticDir)) {
		const staticFiles = await glob('**/*.{md,mdx}', { cwd: contentStaticDir, nodir: true });
		for (const rel of staticFiles) {
			const srcAbs = path.join(contentStaticDir, rel);
			const content = await readFile(srcAbs);
			const docId = docIdFor('', toPosix(rel)).replace(/^\//, '');
			claim(docId, srcAbs);
			const ext = path.extname(rel) || '.md';
			const destAbs = path.join(contentDir, `${docId || 'index'}${ext}`);
			const siblingAbs = path.join(staticDir, `${docId || 'index'}.md`);
			await stageFile({ srcAbs, destAbs, siblingAbs, content, mode });
			manifest.push({ id: docId || 'index', route: docId ? `/${docId}` : '/', title: frontMatterTitle(content) || headingTitle(content) || docId || 'Home', mdSibling: `/${docId || 'index'}.md`, source: srcAbs });
		}
	}

	// 2. Built-in nodes contributor. Each node is staged into a category folder
	//    (by its services.json classType) so the autogenerated sidebar groups
	//    them like the canvas; an injected slug keeps the flat /nodes/<name>
	//    route that redirects.ts and existing links depend on.
	const nodeDocCandidates = await glob(NODES_GLOB, { cwd: projectRoot, nodir: true });
	// One doc per node: README.md counts only when it carries
	// the generated markers (checked below, after reading the content).
	const docByNode = new Map();
	for (const rel of nodeDocCandidates.sort()) {
		const name = toPosix(rel).split('/').slice(-2)[0]; // nodes/src/nodes/<name>/<file>
		docByNode.set(name, rel);
	}
	// Variant sub-pages: a node may carry one README.md per registerable backend
	// in a subdirectory (e.g. index_search/elasticsearch/README.md). When present,
	// the node renders as a folder — the top-level README is the overview index and
	// each variant becomes a nested page, mirroring the on-disk layout and the
	// canvas palette's separate tiles.
	const variantCandidates = await glob('nodes/src/nodes/*/*/README.md', { cwd: projectRoot, nodir: true });
	const variantsByNode = new Map();
	for (const rel of variantCandidates.sort()) {
		const parts = toPosix(rel).split('/'); // nodes/src/nodes/<name>/<variant>/README.md
		const name = parts[parts.length - 3];
		const variant = parts[parts.length - 2];
		if (!variantsByNode.has(name)) variantsByNode.set(name, []);
		variantsByNode.get(name).push({ variant, rel });
	}
	const nodeDocs = [];
	const stagedCategories = new Set();
	for (const [name, rel] of [...docByNode.entries()].sort(([a], [b]) => a.localeCompare(b))) {
		const srcAbs = path.join(projectRoot, rel);
		const nodeDir = path.dirname(srcAbs);
		const content = await readFile(srcAbs);
		if (!content.includes(GENERATED_START)) continue; // legacy README, not a doc
		nodeDocs.push(rel);
		const route = `${NODES_DIR}/${name}`; // flat public route, preserved via slug
		claim(route, srcAbs);
		const { classType, title, description } = await readNodeMeta(nodeDir);
		const cat = NODE_CATEGORIES[classType] || FALLBACK_CATEGORY;
		// Keep an authored front-matter title; only synthesize a canvas name for
		// the raw-named docs that lack one (e.g. text_output, index_search, core).
		const existingTitle = frontMatterTitle(content);
		const label = existingTitle || nodeLabel(name, title);
		// Emit the per-category sidebar heading (label + order) once.
		if (!stagedCategories.has(cat.slug)) {
			stagedCategories.add(cat.slug);
			await writeFileEnsure(path.join(contentDir, NODES_DIR, cat.slug, '_category_.json'), JSON.stringify({ label: cat.label, position: cat.position, collapsed: true }, null, 2));
		}
		const variants = variantsByNode.get(name) || [];
		if (variants.length) {
			// Folder layout: the node becomes a category under its classType group,
			// the top-level README is its index page (flat route preserved via slug),
			// and each backend variant is a nested page. The category links to the
			// index so the parent label is clickable.
			const folderRel = toPosix(path.join(NODES_DIR, cat.slug, name));
			await writeFileEnsure(path.join(contentDir, folderRel, '_category_.json'), JSON.stringify({ label, collapsed: true, link: { type: 'doc', id: `${folderRel}/index` } }, null, 2));
			await writeFileEnsure(path.join(contentDir, folderRel, 'index.md'), stageNodeMarkdown(content, { slug: `/${route}`, title: existingTitle ? null : label }));
			await writeFileEnsure(path.join(staticDir, `${route}.md`), content);
			manifest.push({ id: route, route: `/${route}`, title: label, mdSibling: `/${route}.md`, source: srcAbs, node: name, category: cat.label, categoryOrder: cat.position, description });
			const serviceDescriptions = await readServiceDescriptions(nodeDir);
			for (const { variant, rel: vrel } of variants) {
				const vsrcAbs = path.join(projectRoot, vrel);
				const vcontent = await readFile(vsrcAbs);
				const vroute = `${route}/${variant}`;
				claim(vroute, vsrcAbs);
				nodeDocs.push(vrel);
				const vExistingTitle = frontMatterTitle(vcontent);
				const vlabel = vExistingTitle || nodeLabel(variant, '');
				const vdescription = variantDescription(variant, serviceDescriptions);
				await writeFileEnsure(path.join(contentDir, folderRel, `${variant}.md`), stageNodeMarkdown(vcontent, { slug: `/${vroute}`, title: vExistingTitle ? null : vlabel }));
				await writeFileEnsure(path.join(staticDir, `${vroute}.md`), vcontent);
				manifest.push({ id: vroute, route: `/${vroute}`, title: vlabel, mdSibling: `/${vroute}.md`, source: vsrcAbs, node: name, variant, category: cat.label, categoryOrder: cat.position, description: vdescription });
			}
			continue;
		}
		// Nest the page under its category folder; slug pins the flat route, title
		// fills in when missing, and the body H1 is dropped to avoid a duplicate
		// heading. Always a real write — a symlink can't carry the edits.
		const destAbs = path.join(contentDir, NODES_DIR, cat.slug, `${name}.md`);
		await writeFileEnsure(destAbs, stageNodeMarkdown(content, { slug: `/${route}`, title: existingTitle ? null : label }));
		await writeFileEnsure(path.join(staticDir, `${route}.md`), content);
		manifest.push({ id: route, route: `/${route}`, title: label, mdSibling: `/${route}.md`, source: srcAbs, node: name, category: cat.label, categoryOrder: cat.position, description });
	}

	// 3. Declared per-package mounts.
	const contributors = discoverContributors();
	const allDocsFiles = await glob(DOCS_GLOB, { cwd: projectRoot, nodir: true, ignore: IGNORE });
	for (const rel of allDocsFiles) {
		const abs = path.join(projectRoot, rel);
		const owner = contributors.find((c) => `${abs}${path.sep}`.startsWith(`${c.sourceDir}${path.sep}`));
		if (!owner) {
			throw new Error(`docs:gather: ${rel} lives under a docs/ tree but no package declares a mount covering it. Add a docs mount in the owning package's scripts/tasks.js.`);
		}
		const relUnder = toPosix(path.relative(owner.sourceDir, abs));
		const content = await readFile(abs);
		const docId = docIdFor(owner.mount, relUnder);
		claim(docId, abs);
		const ext = path.extname(relUnder) || '.md';
		const destAbs = path.join(contentDir, `${docId}${ext}`);
		const siblingAbs = path.join(staticDir, `${docId}.md`);
		await stageFile({ srcAbs: abs, destAbs, siblingAbs, content, mode });
		manifest.push({ id: docId, route: `/${docId}`, title: frontMatterTitle(content) || headingTitle(content) || docId, mdSibling: `/${docId}.md`, source: abs, module: owner.module });
	}

	// 4. Placeholders for spine slots still lacking content (keeps the build green).
	await ensurePlaceholders({ contentDir, staticDir, routes, manifest });

	// 5. Persist the manifest for docs:index (Phase 3).
	await writeFileEnsure(path.join(contentDir, '.manifest.json'), JSON.stringify(manifest, null, 2));

	if (task) task.output = `Staged ${manifest.length} pages, ${nodeDocs.length} nodes, ${contributors.length} mounts`;
	return manifest;
}

/**
 * Whether a doc id already resolves to a file (`.md`/`.mdx`, or an `index` under
 * a directory of that id) in the assembled content tree.
 * @param {string} contentDir - assembled content directory.
 * @param {string} id - doc id to probe.
 * @return {Promise<boolean>}
 */
async function docExists(contentDir, id) {
	return (await exists(path.join(contentDir, `${id}.md`))) || (await exists(path.join(contentDir, `${id}.mdx`))) || (await exists(path.join(contentDir, id, 'index.md'))) || (await exists(path.join(contentDir, id, 'index.mdx')));
}

/**
 * Write placeholder pages for every spine slot that still lacks authored or
 * generated content, so unresolved sidebar links do not break the build.
 * @param {object} args
 * @param {string} args.contentDir - assembled content directory.
 * @param {string} args.staticDir - directory for raw `.md` siblings.
 * @param {Map<string, string>} args.routes - id -> staged file path, updated in place.
 * @param {Array<object>} args.manifest - manifest entries, appended in place.
 * @return {Promise<void>}
 */
async function ensurePlaceholders({ contentDir, staticDir, routes, manifest }) {
	const titles = docTitles();
	for (const id of allDocIds()) {
		if (id === 'index') continue;
		if (routes.has(id) || (await docExists(contentDir, id))) continue;
		const title = titles[id] || id;
		const content = `---\ntitle: ${title}\n---\n\n# ${title}\n\n${PLACEHOLDER_NOTE}\n`;
		const contentFile = path.join(contentDir, `${id}.md`);
		await writeFileEnsure(contentFile, content);
		await writeFileEnsure(path.join(staticDir, `${id}.md`), content);
		routes.set(id, contentFile);
		manifest.push({ id, route: `/${id}`, title, mdSibling: `/${id}.md`, source: contentFile, placeholder: true });
	}

	// Nodes catalog: ensure a landing page (docs:index overwrites it); a
	// placeholder entry only if no real node pages exist.
	const nodesDir = path.join(contentDir, NODES_DIR);
	if (!(await exists(path.join(nodesDir, 'index.md')))) {
		await writeFileEnsure(path.join(nodesDir, 'index.md'), `---\ntitle: Nodes\nslug: /${NODES_DIR}\nsidebar_position: 0\n---\n\n# Nodes\n\n${PLACEHOLDER_NOTE}\n`);
	}
	const hasNodePages = (await glob('**/*.md', { cwd: nodesDir, nodir: true })).some((f) => f !== 'index.md');
	if (!hasNodePages) {
		const content = `---\ntitle: Example node\nsidebar_position: 1\n---\n\n# Example node\n\n${PLACEHOLDER_NOTE}\n`;
		const contentFile = path.join(nodesDir, 'example.md');
		await writeFileEnsure(contentFile, content);
		await writeFileEnsure(path.join(staticDir, `${NODES_DIR}/example.md`), content);
		manifest.push({ id: `${NODES_DIR}/example`, route: `/${NODES_DIR}/example`, title: 'Example node', mdSibling: `/${NODES_DIR}/example.md`, source: contentFile, node: 'example', placeholder: true });
	}
}

