import { describe, it } from 'node:test';
import assert from 'node:assert/strict';
import spine from '../../scripts/lib/spine.js';

const { toSidebar, isValidMount, sectionFor, allDocIds, docTitles, NODES_DIR } = spine;

describe('toSidebar', () => {
	const { docsSidebar } = toSidebar();

	it('returns a { docsSidebar } array', () => {
		assert.ok(Array.isArray(docsSidebar));
		assert.ok(docsSidebar.length > 0);
	});

	it('renders a labeled leaf as { type: doc, id, label } so the spine label wins', () => {
		const home = docsSidebar.find((n) => n && n.type === 'doc' && n.id === 'index');
		assert.ok(home, 'index leaf present');
		assert.equal(home.label, 'Home');
	});

	it('renders a category as { type: category, label, items }', () => {
		const concepts = docsSidebar.find((n) => n && n.type === 'category' && n.label === 'Concepts');
		assert.ok(concepts, 'Concepts category present');
		assert.ok(Array.isArray(concepts.items) && concepts.items.length > 0);
		// leaves inside a category carry their spine label too
		const pipelines = concepts.items.find((n) => n && n.type === 'doc' && n.id === 'concepts/pipelines');
		assert.ok(pipelines, 'concepts/pipelines leaf present');
		assert.equal(pipelines.label, 'Pipelines');
	});

	it('renders the autogenerated Nodes catalog', () => {
		const nodes = docsSidebar.find((n) => n && n.type === 'category' && n.label === 'Nodes');
		assert.ok(nodes);
		assert.deepEqual(nodes.items, [{ type: 'autogenerated', dirName: NODES_DIR }]);
	});
});

describe('isValidMount', () => {
	it('accepts an exact mount slot', () => {
		assert.equal(isValidMount('protocols/websocket'), true);
		assert.equal(isValidMount(NODES_DIR), true);
	});

	it('accepts a descendant of a slot and tolerates slashes', () => {
		assert.equal(isValidMount('develop/typescript/reference'), true);
		assert.equal(isValidMount('/protocols/websocket/'), true);
	});

	it('rejects a path that is not a slot', () => {
		assert.equal(isValidMount('protocols'), false);
		assert.equal(isValidMount('not/a/slot'), false);
	});
});

describe('sectionFor', () => {
	it('maps a doc id to its owning top-level section', () => {
		assert.equal(sectionFor('concepts/pipelines'), 'Concepts');
		assert.equal(sectionFor('nodes/webhook'), 'Nodes');
		assert.equal(sectionFor('protocols/websocket'), 'Protocols');
	});

	it('falls back to Other for an unknown id', () => {
		assert.equal(sectionFor('totally/unknown'), 'Other');
	});
});

describe('allDocIds / docTitles', () => {
	it('gives every authored id a title', () => {
		const ids = allDocIds();
		const titles = docTitles();
		assert.ok(ids.length > 0);
		for (const id of ids) {
			assert.ok(titles[id], `title for ${id}`);
		}
		assert.equal(Object.keys(titles).length, ids.length);
	});
});
