"""
/task/fetch — JWT-authenticated file download endpoint.

Serves files from the local filesystem store using short-lived JWT tokens
generated by ``FileStore.get_url()``. Cloud backends (S3, Azure) return
presigned URLs directly and bypass this endpoint entirely.

Features:
    - JWT validation with HS256 (signed with RR_SIGNING_KEY)
    - FileResponse for proper Content-Type and Range request support
    - No manual streaming — FastAPI/Starlette handles everything
"""

import os

import jwt
from fastapi import Request
from fastapi.responses import FileResponse, JSONResponse


async def handle_fetch(request: Request):
    """
    Serve a file from the filesystem store, authenticated via JWT query param.

    The JWT payload must contain:
        - ``sub``: userId (scopes the file store)
        - ``path``: relative file path within the user's store
        - ``exp``: expiration timestamp (standard JWT claim)
    """
    # ── Extract and validate JWT ─────────────────────────────────────────
    token = request.query_params.get('token')
    if not token:
        return JSONResponse({'error': 'Missing token parameter'}, status_code=401)

    signing_key = os.environ.get('RR_SIGNING_KEY', '')
    if not signing_key:
        return JSONResponse({'error': 'Server signing key not configured'}, status_code=500)

    try:
        # Require the `exp` claim so a signed token without an expiration is
        # rejected rather than treated as non-expiring.
        payload = jwt.decode(token, signing_key, algorithms=['HS256'], options={'require': ['exp']})
    except jwt.ExpiredSignatureError:
        return JSONResponse({'error': 'Token expired'}, status_code=401)
    except jwt.InvalidTokenError as e:
        return JSONResponse({'error': f'Invalid token: {e}'}, status_code=401)

    user_id = payload.get('sub')
    path = payload.get('path')
    download_name = payload.get('download_name')
    if not user_id or not path:
        return JSONResponse({'error': 'Token missing required claims'}, status_code=400)

    # ── Resolve the absolute file path via the store ─────────────────────
    task_server = request.app.state.task
    file_store = task_server.store.get_file_store(user_id)

    # Build the full storage path and resolve it through the filesystem backend.
    # A malformed `path` claim can raise ValueError; return 400 rather than 500.
    try:
        full_store_path = file_store._full_path(path)
    except ValueError:
        return JSONResponse({'error': 'Invalid path'}, status_code=400)
    backend = file_store._store
    try:
        abs_path = backend._get_full_path(full_store_path)
    except Exception:
        return JSONResponse({'error': 'File not found'}, status_code=404)

    if not abs_path.is_file():
        return JSONResponse({'error': 'File not found'}, status_code=404)

    # ── Serve the file ───────────────────────────────────────────────────
    # FileResponse handles Content-Type (from extension), Content-Length,
    # Range requests (HTTP 206), and streaming automatically.
    #
    # If the token carries a download_name, force a download with that filename
    # (Content-Disposition: attachment). Otherwise serve inline so media
    # viewers can stream the file (video/audio/pdf) in the browser.
    if download_name:
        return FileResponse(
            path=str(abs_path),
            filename=download_name,
            headers={
                'Cache-Control': 'private, max-age=3600',
                'Accept-Ranges': 'bytes',
            },
        )
    return FileResponse(
        path=str(abs_path),
        content_disposition_type='inline',
        headers={
            'Cache-Control': 'private, max-age=3600',
            'Accept-Ranges': 'bytes',
        },
    )
